  // Hàm trả về thông tin dinh dưỡng cho một món ăn dựa trên tên
  static FoodItem? getFoodByName(String name) {
    final foods = getVietnameseFoodItems();
    
    // Tìm kiếm chính xác
    for (var food in foods) {
      if (food.name.toLowerCase() == name.toLowerCase()) {
        return food;
      }
    }
    
    // Tìm kiếm mờ
    for (var food in foods) {
      if (food.name.toLowerCase().contains(name.toLowerCase()) ||
          name.toLowerCase().contains(food.name.toLowerCase())) {
        return food;
      }
    }
    
    return null;
  }
